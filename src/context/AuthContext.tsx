import React, { createContext, useContext, useEffect, useState } from 'react';
import { User, Session, AuthError } from '@supabase/supabase-js';
import { supabase, Profile } from '../lib/supabase';

interface AuthContextType {
  user: User | null;
  profile: Profile | null;
  session: Session | null;
  isAuthenticated: boolean;
  isAuthLoading: boolean;
  isInitialAuthCheckDone: boolean;
  login: (email: string, password: string) => Promise<{ success: boolean; error?: string }>;
  register: (name: string, email: string, password: string) => Promise<{ success: boolean; error?: string }>;
  logout: () => Promise<void>;
  resetPassword: (email: string) => Promise<{ success: boolean; error?: string }>;
  updateProfile: (updates: Partial<Profile>) => Promise<{ success: boolean; error?: string }>;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};

interface AuthProviderProps {
  children: React.ReactNode;
}

export const AuthProvider: React.FC<AuthProviderProps> = ({ children }) => {
  const [user, setUser] = useState<User | null>(null);
  const [profile, setProfile] = useState<Profile | null>(null);
  const [session, setSession] = useState<Session | null>(null);
  const [isAuthLoading, setIsAuthLoading] = useState(false);
  const [isInitialAuthCheckDone, setIsInitialAuthCheckDone] = useState(false);

  // Buscar ou criar perfil do usu√°rio
  const fetchOrCreateProfile = async (user: User) => {
    console.log('üîç AuthContext: fetchOrCreateProfile - Iniciando para userId:', user.id);
    console.log('üîç AuthContext: fetchOrCreateProfile - User metadata:', user.user_metadata);
    console.log('üîç AuthContext: fetchOrCreateProfile - User email:', user.email);
    
    try {
      // Primeiro, tentar buscar o perfil existente
      console.log('üîç AuthContext: fetchOrCreateProfile - Tentando buscar perfil existente...');
      
      const { data: existingProfile, error: fetchError } = await supabase
        .from('profiles')
        .select('*')
        .eq('id', user.id)
        .single();

      console.log('üîç AuthContext: fetchOrCreateProfile - Resultado da busca:');
      console.log('  - existingProfile:', existingProfile);
      console.log('  - fetchError:', fetchError);
      console.log('  - fetchError code:', fetchError?.code);
      console.log('  - fetchError message:', fetchError?.message);

      if (fetchError && fetchError.code !== 'PGRST116') {
        // PGRST116 √© o c√≥digo para "nenhuma linha encontrada", outros erros s√£o problemas reais
        console.error('‚ùå AuthContext: fetchOrCreateProfile - Erro ao buscar perfil:', fetchError);
        console.error('‚ùå AuthContext: fetchOrCreateProfile - Detalhes do erro:', {
          code: fetchError.code,
          message: fetchError.message,
          details: fetchError.details,
          hint: fetchError.hint
        });
        return null;
      }

      if (existingProfile) {
        console.log('‚úÖ AuthContext: fetchOrCreateProfile - Perfil existente encontrado:', existingProfile);
        return existingProfile;
      }

      // Se n√£o encontrou o perfil, criar um novo
      console.log('üîß AuthContext: fetchOrCreateProfile - Perfil n√£o encontrado, criando novo perfil...');
      
      const newProfile: Omit<Profile, 'created_at' | 'updated_at'> = {
        id: user.id,
        name: user.user_metadata?.name || user.email?.split('@')[0] || 'Usu√°rio',
        email: user.email || '',
        avatar_url: user.user_metadata?.avatar_url || null,
      };

      console.log('üîß AuthContext: fetchOrCreateProfile - Dados do novo perfil:', newProfile);

      const { data: createdProfile, error: createError } = await supabase
        .from('profiles')
        .insert([newProfile])
        .select()
        .single();

      console.log('üîß AuthContext: fetchOrCreateProfile - Resultado da cria√ß√£o:');
      console.log('  - createdProfile:', createdProfile);
      console.log('  - createError:', createError);

      if (createError) {
        console.error('‚ùå AuthContext: fetchOrCreateProfile - Erro ao criar perfil:', createError);
        console.error('‚ùå AuthContext: fetchOrCreateProfile - Detalhes do erro de cria√ß√£o:', {
          code: createError.code,
          message: createError.message,
          details: createError.details,
          hint: createError.hint
        });
        return null;
      }

      console.log('‚úÖ AuthContext: fetchOrCreateProfile - Novo perfil criado com sucesso:', createdProfile);
      return createdProfile;
    } catch (error) {
      console.error('üí• AuthContext: fetchOrCreateProfile - Erro inesperado:', error);
      console.error('üí• AuthContext: fetchOrCreateProfile - Stack trace:', error instanceof Error ? error.stack : 'No stack trace');
      return null;
    }
  };

  // Verificar sess√£o atual e configurar listener
  useEffect(() => {
    console.log('üöÄ AuthContext: Iniciando useEffect de autentica√ß√£o...');
    
    // Verificar sess√£o atual
    const getSession = async () => {
      console.log('üîê AuthContext: Iniciando verifica√ß√£o de sess√£o inicial...');
      try {
        const { data: { session }, error } = await supabase.auth.getSession();
        
        console.log('üîê AuthContext: getSession - Resultado:');
        console.log('  - session:', session);
        console.log('  - error:', error);
        
        if (error) {
          console.error('‚ùå AuthContext: Erro ao obter sess√£o:', error);
          console.error('‚ùå AuthContext: Detalhes do erro de sess√£o:', {
            message: error.message,
            status: error.status
          });
          // Se h√° erro ao obter sess√£o (como refresh token inv√°lido), limpar sess√£o local
          console.log('üßπ AuthContext: Limpando sess√£o local devido ao erro');
          await supabase.auth.signOut();
          setSession(null);
          setUser(null);
          setProfile(null);
        } else {
          console.log('üîê AuthContext: Sess√£o obtida com sucesso');
          setSession(session);
          setUser(session?.user ?? null);
          
          if (session?.user) {
            console.log('üë§ AuthContext: Sess√£o inicial encontrada, buscando/criando perfil...');
            console.log('üë§ AuthContext: User ID da sess√£o:', session.user.id);
            
            const userProfile = await fetchOrCreateProfile(session.user);
            console.log('üë§ AuthContext: Resultado do fetchOrCreateProfile:', userProfile);
            
            setProfile(userProfile);
            console.log('‚úÖ AuthContext: Perfil carregado/criado na sess√£o inicial:', userProfile);
          } else {
            console.log('üö´ AuthContext: Nenhuma sess√£o inicial encontrada.');
          }
        }
      } catch (error) {
        console.error('üí• AuthContext: Erro inesperado ao obter sess√£o:', error);
        console.error('üí• AuthContext: Stack trace da sess√£o:', error instanceof Error ? error.stack : 'No stack trace');
        // Em caso de erro inesperado, tamb√©m limpar a sess√£o
        console.log('üßπ AuthContext: Limpando sess√£o local devido ao erro inesperado');
        await supabase.auth.signOut();
        setSession(null);
        setUser(null);
        setProfile(null);
      } finally {
        setIsInitialAuthCheckDone(true);
        console.log('‚úÖ AuthContext: Verifica√ß√£o de autentica√ß√£o inicial conclu√≠da.');
      }
    };

    getSession();

    // Listener para mudan√ßas de autentica√ß√£o
    console.log('üëÇ AuthContext: Configurando listener de mudan√ßas de autentica√ß√£o...');
    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        console.log('üîÑ AuthContext: Mudan√ßa de estado de autentica√ß√£o detectada:');
        console.log('  - event:', event);
        console.log('  - session:', session);
        console.log('  - user:', session?.user);
        
        // Se a sess√£o se tornou null mas o evento n√£o √© SIGNED_OUT, pode ser um token inv√°lido
        if (!session && event !== 'SIGNED_OUT') {
          console.log('üßπ AuthContext: Sess√£o perdida sem logout expl√≠cito, limpando dados locais');
          await supabase.auth.signOut();
        }
        
        setSession(session);
        setUser(session?.user ?? null);
        
        if (session?.user) {
          console.log('üë§ AuthContext: onAuthStateChange - Usu√°rio autenticado, buscando/criando perfil...');
          console.log('üë§ AuthContext: onAuthStateChange - User ID:', session.user.id);
          
          const userProfile = await fetchOrCreateProfile(session.user);
          console.log('üë§ AuthContext: onAuthStateChange - Resultado do fetchOrCreateProfile:', userProfile);
          
          setProfile(userProfile);
          console.log('‚úÖ AuthContext: Perfil carregado/criado ap√≥s mudan√ßa de estado:', userProfile);
        } else {
          console.log('üö´ AuthContext: Usu√°rio desautenticado.');
          setProfile(null);
        }
        
        setIsInitialAuthCheckDone(true);
        console.log('‚úÖ AuthContext: onAuthStateChange conclu√≠do, isInitialAuthCheckDone = true.');
      }
    );

    return () => {
      console.log('üßπ AuthContext: Limpando subscription...');
      subscription.unsubscribe();
    };
  }, []);

  const login = async (email: string, password: string): Promise<{ success: boolean; error?: string }> => {
    console.log('üîë AuthContext: Iniciando login para:', email);
    try {
      setIsAuthLoading(true);
      
      const { data, error } = await supabase.auth.signInWithPassword({
        email,
        password,
      });

      console.log('üîë AuthContext: Resultado do signInWithPassword:');
      console.log('  - data:', data);
      console.log('  - error:', error);

      if (error) {
        console.error('‚ùå AuthContext: Erro no login:', error);
        console.error('‚ùå AuthContext: Detalhes do erro de login:', {
          message: error.message,
          status: error.status
        });
        return { success: false, error: getErrorMessage(error) };
      }

      console.log('‚úÖ AuthContext: Login bem-sucedido:', data);
      return { success: true };
    } catch (error) {
      console.error('üí• AuthContext: Erro inesperado no login:', error);
      console.error('üí• AuthContext: Stack trace do login:', error instanceof Error ? error.stack : 'No stack trace');
      return { success: false, error: 'Erro inesperado ao fazer login' };
    } finally {
      setIsAuthLoading(false);
      console.log('üèÅ AuthContext: Login finalizado.');
    }
  };

  const register = async (name: string, email: string, password: string): Promise<{ success: boolean; error?: string }> => {
    console.log('üìù AuthContext: Iniciando registro para:', email, 'com nome:', name);
    try {
      setIsAuthLoading(true);
      
      const { data, error } = await supabase.auth.signUp({
        email,
        password,
        options: {
          data: {
            name: name,
          },
        },
      });

      console.log('üìù AuthContext: Resultado do signUp:');
      console.log('  - data:', data);
      console.log('  - error:', error);

      if (error) {
        console.error('‚ùå AuthContext: Erro no registro:', error);
        console.error('‚ùå AuthContext: Detalhes do erro de registro:', {
          message: error.message,
          status: error.status
        });
        return { success: false, error: getErrorMessage(error) };
      }

      console.log('üìù AuthContext: Registro realizado:', data);

      // Se o usu√°rio foi criado mas precisa confirmar email
      if (data.user && !data.session) {
        console.log('üìß AuthContext: Usu√°rio criado mas precisa confirmar email');
        return { 
          success: false, 
          error: 'Verifique seu email para confirmar a conta antes de fazer login' 
        };
      }

      console.log('‚úÖ AuthContext: Registro bem-sucedido com sess√£o ativa');
      return { success: true };
    } catch (error) {
      console.error('üí• AuthContext: Erro inesperado no registro:', error);
      console.error('üí• AuthContext: Stack trace do registro:', error instanceof Error ? error.stack : 'No stack trace');
      return { success: false, error: 'Erro inesperado ao criar conta' };
    } finally {
      setIsAuthLoading(false);
      console.log('üèÅ AuthContext: Registro finalizado.');
    }
  };

  const logout = async (): Promise<void> => {
    console.log('üö™ AuthContext: Iniciando logout...');
    try {
      setIsAuthLoading(true);
      const { error } = await supabase.auth.signOut();
      
      console.log('üö™ AuthContext: Resultado do signOut:');
      console.log('  - error:', error);
      
      if (error) {
        console.error('‚ùå AuthContext: Erro no logout:', error);
      } else {
        console.log('‚úÖ AuthContext: Logout bem-sucedido');
      }
    } catch (error) {
      console.error('üí• AuthContext: Erro inesperado no logout:', error);
      console.error('üí• AuthContext: Stack trace do logout:', error instanceof Error ? error.stack : 'No stack trace');
    } finally {
      setIsAuthLoading(false);
      console.log('üèÅ AuthContext: Logout finalizado.');
    }
  };

  const resetPassword = async (email: string): Promise<{ success: boolean; error?: string }> => {
    console.log('üîÑ AuthContext: Iniciando reset de senha para:', email);
    try {
      setIsAuthLoading(true);
      const { error } = await supabase.auth.resetPasswordForEmail(email, {
        redirectTo: `${window.location.origin}/reset-password`,
      });

      console.log('üîÑ AuthContext: Resultado do resetPasswordForEmail:');
      console.log('  - error:', error);

      if (error) {
        console.error('‚ùå AuthContext: Erro no reset de senha:', error);
        return { success: false, error: getErrorMessage(error) };
      }

      console.log('‚úÖ AuthContext: Reset de senha enviado com sucesso');
      return { success: true };
    } catch (error) {
      console.error('üí• AuthContext: Erro inesperado no reset de senha:', error);
      console.error('üí• AuthContext: Stack trace do reset:', error instanceof Error ? error.stack : 'No stack trace');
      return { success: false, error: 'Erro inesperado ao enviar email de recupera√ß√£o' };
    } finally {
      setIsAuthLoading(false);
      console.log('üèÅ AuthContext: Reset de senha finalizado.');
    }
  };

  const updateProfile = async (updates: Partial<Profile>): Promise<{ success: boolean; error?: string }> => {
    console.log('‚úèÔ∏è AuthContext: Iniciando atualiza√ß√£o de perfil:', updates);
    try {
      setIsAuthLoading(true);
      if (!user) {
        console.error('‚ùå AuthContext: Tentativa de atualizar perfil sem usu√°rio autenticado');
        return { success: false, error: 'Usu√°rio n√£o autenticado' };
      }

      const { error } = await supabase
        .from('profiles')
        .update(updates)
        .eq('id', user.id);

      console.log('‚úèÔ∏è AuthContext: Resultado da atualiza√ß√£o:');
      console.log('  - error:', error);

      if (error) {
        console.error('‚ùå AuthContext: Erro ao atualizar perfil:', error);
        console.error('‚ùå AuthContext: Detalhes do erro de atualiza√ß√£o:', {
          code: error.code,
          message: error.message,
          details: error.details,
          hint: error.hint
        });
        return { success: false, error: 'Erro ao atualizar perfil' };
      }

      // Atualizar estado local
      if (profile) {
        const updatedProfile = { ...profile, ...updates };
        setProfile(updatedProfile);
        console.log('‚úÖ AuthContext: Perfil atualizado localmente:', updatedProfile);
      }

      console.log('‚úÖ AuthContext: Perfil atualizado com sucesso');
      return { success: true };
    } catch (error) {
      console.error('üí• AuthContext: Erro inesperado ao atualizar perfil:', error);
      console.error('üí• AuthContext: Stack trace da atualiza√ß√£o:', error instanceof Error ? error.stack : 'No stack trace');
      return { success: false, error: 'Erro inesperado ao atualizar perfil' };
    } finally {
      setIsAuthLoading(false);
      console.log('üèÅ AuthContext: Atualiza√ß√£o de perfil finalizada.');
    }
  };

  // Fun√ß√£o para converter erros do Supabase em mensagens amig√°veis
  const getErrorMessage = (error: AuthError): string => {
    console.log('üîÑ AuthContext: Convertendo erro:', error.message);
    switch (error.message) {
      case 'Invalid login credentials':
        return 'Email ou senha incorretos';
      case 'Email not confirmed':
        return 'Email n√£o confirmado. Verifique sua caixa de entrada';
      case 'User already registered':
        return 'Este email j√° est√° cadastrado';
      case 'Password should be at least 6 characters':
        return 'A senha deve ter pelo menos 6 caracteres';
      case 'Unable to validate email address: invalid format':
        return 'Formato de email inv√°lido';
      case 'Signup is disabled':
        return 'Cadastro temporariamente desabilitado';
      default:
        return error.message || 'Erro desconhecido';
    }
  };

  const value = {
    user,
    profile,
    session,
    isAuthenticated: !!user,
    isAuthLoading,
    isInitialAuthCheckDone,
    login,
    register,
    logout,
    resetPassword,
    updateProfile,
  };

  console.log('üìä AuthContext: Estado atual:', {
    user: !!user,
    profile: !!profile,
    session: !!session,
    isAuthenticated: !!user,
    isAuthLoading,
    isInitialAuthCheckDone,
    userId: user?.id,
    userEmail: user?.email,
    profileId: profile?.id,
    profileName: profile?.name
  });

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  );
};